export type Messages = (typeof translations)[Locale];

export function hasLocale(value: string | null | undefined): value is Locale {
  return value === "nl" || value === "en";
}

function interpolate(template: string, values: Record<string, string>) {
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, value),
    template,
  );
}

export function getSteps(locale: Locale) {
  const stepTranslations = translations[locale].steps;
  return [
    { id: "participants" as const, ...stepTranslations.participants },
    { id: "costs" as const, ...stepTranslations.costs },
    { id: "review" as const, ...stepTranslations.review },
    { id: "payments" as const, ...stepTranslations.payments },
  ];
}

export function getDistributionOptions(locale: Locale) {
  const items = translations[locale].distribution;
  return [
    { value: "per_night" as const, label: items.per_night },
    { value: "per_stay_person" as const, label: items.per_stay_person },
    { value: "headcount" as const, label: items.headcount },
    { value: "direct_household" as const, label: items.direct_household },
    { value: "excluded" as const, label: items.excluded },
  ];
}

export function getDistributionLabel(
  locale: Locale,
  value: "per_night" | "per_stay_person" | "headcount" | "direct_household" | "excluded",
) {
  return translations[locale].distribution[value];
}

export function formatValidationMessage(
  locale: Locale,
  key: keyof (typeof translations)[Locale]["validation"],
  values: Record<string, string> = {},
) {
  return interpolate(translations[locale].validation[key], values);
}

export function formatSelectorMessage(
  locale: Locale,
  key: keyof (typeof translations)[Locale]["selectors"],
  values: Record<string, string> = {},
) {
  return interpolate(translations[locale].selectors[key], values);
}
